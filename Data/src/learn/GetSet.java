package learn;

public class GetSet {
	public String name;
	public int age;
	public String favColor;
	
	//setters
	public void setname(String name) {
		this.name = name;
	}
	public void setage(int age) {
		this.age = age;
	}
	public void setfavColor(String favColor) {
		this.favColor = favColor;
	}
	
	//getters
	public String getname() {
		return name;
	}
	public int getage() {
		return age;
	}
	public String getfavColor() {
		return favColor;
	}

}
